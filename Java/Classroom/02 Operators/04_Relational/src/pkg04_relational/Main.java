
package pkg04_relational;

public class Main 
{
    public static void main(String[] args) 
    {
        boolean flag1 = (2 == 5);       // Equals To
        boolean flag2 = (2 <= 5);       // Less Than Equals To
        boolean flag3 = (2 >= 5);       // Greater Than Equals To
        boolean flag4 = (2 < 5);        // Less Than
        boolean flag5 = (2 > 5);        // Greater Than
        boolean flag6 = (2 != 5);       // Not Equals To
        
        System.out.println(flag1);
        System.out.println(flag2);
        System.out.println(flag3);
        System.out.println(flag4);
        System.out.println(flag5);
        System.out.println(flag6);
    }
}
