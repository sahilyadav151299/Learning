
package pkg02_arithmetic;

public class Main 
{
    public static void main(String[] args) 
    {
        int n1 = 100;
        int n2 = 20;
        
        System.out.println("n1 + n2 = " + (n1 + n2));    // Binary +
        System.out.println("n1 - n2 = " + (n1 - n2));    // Binary -
        System.out.println("n1 * n2 = " + (n1 * n2));    // Multiplication Operator
        System.out.println("n1 / n2 = " + (n1 / n2));    // Division Operator
        System.out.println("n1 % n2 = " + (n1 % n2));    // Modulo Operator
        
        System.out.println(9/2);
        System.out.println(9.0/2);
        System.out.println(9/2.0);
        System.out.println(9.0/2.0);
    }   
}
